ASSUME CS:CODE, DS:DATA
DATA SEGMENT
	N1 DW 02H
	RESULT DW 2 DUP(?)
	MSG DB 'RESULT: $'
	COUNT DW 1 DUP(?)
DATA ENDS
CODE SEGMENT
START:
	MOV AX, DATA
	MOV DS, AX
	XOR DX, DX
	MOV AX, N1
	MOV BX, N1
	MUL BX
	MOV RESULT, AX
	MOV RESULT+2, DX
	CMP AX, DX
	JZ STOP
	
	LEA DX, MSG
	MOV AH, 09H
	INT 21H
	MOV AX, 01H
	MOV COUNT, AX
	MOV DX, RESULT

NEXT:
	MOV CL, 04H

	MOV BX, DX
	AND BH, 0F0H
	SHR BH, 4
	ADD BH, 30H
	DAA
	MOV DL, BH
	MOV AH, 02H
	INT 21H

	MOV BX, DX
	AND BH, 0FH
	ADD BH, 30H
	DAA
	MOV DL, BH
	INT 21H

	MOV BX, DX
	AND BL, 0F0H
	SHR BL, CL
	ADD BL, 30H
	DAA
	MOV DL, BL
	INT 21H

	MOV BX, DX
	AND BL, 0FH
	ADD BL, 30H
	DAA
	MOV DL, BL
	INT 21H

	MOV DX, RESULT+2
	MOV AX, COUNT
	CMP AX, 00H
	JZ STOP
	DEC AX
	MOV COUNT, AX
	JMP NEXT

STOP:
	MOV DL, 48H
	MOV AH, 02H
	INT 21H

	MOV AH, 4CH
	INT 21H
CODE ENDS
END START

